    let rand_number = rand::thread_rng().gen_range(1..=100);

    println!("the rand number is {rand_number}");

    println!("Please input a guess");
    let mut guess = String::new();
    io::stdin()
        .read_line(&mut guess)
        .expect("read_line fail");
    

